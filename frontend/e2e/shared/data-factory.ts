// ─── Rent transaction generator ───────────────────────────────────────────────
// Generates one INCOME transaction per occupied month for a given lease.

export interface RentTransactionParams {
  tenantName: string;
  tenantIban: string;
  monthlyRent: number;
  monthlyCharges: number;
  leaseStartDate: Date;
  /** use lease end date or today, whichever is earlier */
  leaseEndDate: Date;
  buildingIndex: number;
  unitIndex: number;
}

export function generateRentTransactions(p: RentTransactionParams): Array<{
  direction: "INCOME";
  subcategoryName: "Loyer";
  transactionDate: string;
  accountingMonth: string;
  amount: number;
  description: string;
  counterpartyAccount: string;
  buildingIndex: number;
  unitIndex: number;
}> {
  const result = [];
  const ceiling = p.leaseEndDate < new Date() ? p.leaseEndDate : new Date();

  const cursor = new Date(p.leaseStartDate);
  cursor.setDate(5); // paid on the 5th

  while (cursor <= ceiling) {
    const first = new Date(cursor);
    first.setDate(1);
    const dateStr = isoDate(cursor);
    const monthStr = isoDate(first);
    const label = cursor.toLocaleString("fr-BE", {
      month: "long",
      year: "numeric",
    });
    result.push({
      direction: "INCOME" as const,
      subcategoryName: "Loyer" as const,
      transactionDate: dateStr,
      accountingMonth: monthStr,
      amount: p.monthlyRent + p.monthlyCharges,
      description: `Loyer + charges ${label}`,
      counterpartyName: p.tenantName,
      counterpartyAccount: p.tenantIban,
      buildingIndex: p.buildingIndex,
      unitIndex: p.unitIndex,
    });
    cursor.setMonth(cursor.getMonth() + 1);
  }
  return result;
}

