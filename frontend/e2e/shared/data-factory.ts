export function generateRooms(totalSurface: number) {
  const rooms = [
    {
      roomType: "LIVING_ROOM",
      approximateSurface: Math.round(totalSurface * 0.3),
    },
    { roomType: "BEDROOM", approximateSurface: Math.round(totalSurface * 0.2) },
    {
      roomType: "KITCHEN",
      approximateSurface: Math.round(totalSurface * 0.15),
    },
    {
      roomType: "BATHROOM",
      approximateSurface: Math.round(totalSurface * 0.1),
    },
  ];
  if (totalSurface >= 80) {
    rooms.push({
      roomType: "BEDROOM",
      approximateSurface: Math.round(totalSurface * 0.18),
    });
  }
  return rooms;
}

